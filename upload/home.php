                        Geez African Hair Show is designed to rejuvenate and improve on the existing progress of the Hair and Beauty industry in Africa by projecting and promoting skillful practitioners /stylist through a well-thought over reality Television series.
                        <br><br>
                        Geez African Hair Reality TV Show is a yearly themed show aimed to showcase hair stylists both individuals and companies who have got skills, to compete amongst themselves on the application physically at a venue where the ultimate person takes home the prize.
                        <br><br>
                        Geez African Hair Reality TV Show is a product of Geez, which is set up to empower hair stylists and highlight the fashion business viability in Africa. It aims to reveal the realities that doing business in Africa entails to a global audience, and show the creativity, flexibility and innovation of Nigerian and African hair stylists on how they navigate a challenging business terrain and how they intend to scale different challenges they may face in the course of doing their business. This will provide viewing content that is both entertaining and value-adding, while being informative and inspirational.
                        <br><br>
                        The maiden edition of the Geez African Hair Reality Tv Show is set to premiere on the 9th of July, 2022.
